/// Kerosene — Spacing System (8pt Grid)
/// All margins and paddings should rely on these constants.
///
/// `xs` is intentionally kept as the only half-step for dense icon gaps,
/// strokes, and tiny inline offsets. Layout spacing starts at `sm`.
class AppSpacing {
  static const double xs = 4.0;
  static const double sm = 8.0;
  static const double md = 16.0;
  static const double lg = 24.0;
  static const double xl = 32.0;
  static const double xxl = 40.0;
  static const double xxxl = 48.0;
}
